class Assign{
    public static void main(String a[]){
        int num = 20;
        //Post increment
        int result1 = num++;
        System.out.println(result1);
        // preincrement
        int result2 = ++num;
        System.out.println(result2);

        // RELATIONAL OPERATOR
        int x =5;
        int y = 6;
        int z = 5;
        boolean ans = x<y & x ==z & y<z;
        System.out.println(ans);

    }

}